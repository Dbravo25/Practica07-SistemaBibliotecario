    private void btEliminarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarLibroActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(txtCodigo1.getText());
        int respuesta= JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar el Libro");
        if(respuesta == JOptionPane.YES_OPTION){
            Libro libro = libroControlador.read(codigo);
        libroControlador.delete(codigo, libro.getNombiblioteca());
        JOptionPane.showMessageDialog(this, "Se ha eliminado correctamente!");
        limpiarDatos();
        }
            
        
    }//GEN-LAST:event_btEliminarLibroActionPerformed

