    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(txtCodigo1.getText());
        Libro libro = libroControlador.read(codigo);
        if(libro!= null){
            txtLibroAutor.setText(libro.getAutor());
            txtLibroAño.setText(String.valueOf(libro.getAño()));
            txtLibroDisponibleBuscar.setText(String.valueOf(libro.getDisponible()));
            txtLibroGenero.setText(libro.getGenero());
            txtLibroTitulo.setText(libro.getTitulo());
            txtCodigo1.setEditable(false);

        }else{
            JOptionPane.showMessageDialog(this, "El libro a eliminar no existe!");
        }
        
    }
    public void limpiarDatos(){
        txtLibroTitulo.setText("");
        txtLibroAutor.setText("");
        txtLibroAño.setText("");
        txtLibroDisponibleBuscar.setText("");
        txtLibroGenero.setText("");
        txtCodigo1.setText("");
        txtCodigo1.setEditable(true);

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        // TODO add your handling code here:
        limpiarDatos();
    }//GEN-LAST:event_btnLimpiarActionPerformed

