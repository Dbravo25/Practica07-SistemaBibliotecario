    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // TODO add your handling code here:
       int codigo = Integer.parseInt(txtCodigoEliminar.getText());
       Biblioteca biblioteca = bibliotecaControlador.read(codigo);
       if(biblioteca != null){
           txtNombre.setText(biblioteca.getNombre());
           txtDireccion.setText(biblioteca.getDireccion());
           txtTelefono.setText(biblioteca.getTelefono());
           txtCodigoEliminar.setEditable(false);
       }else{
           JOptionPane.showMessageDialog(this, "La biblioteca no existe!");
       }
    }
       
    public void limpiarDatos(){
        txtCodigoEliminar.setText("");
        txtNombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        txtCodigoEliminar.setEditable(true);
    
       
    }//GEN-LAST:event_btBuscarActionPerformed

    private void bnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnLimpiarActionPerformed
        // TODO add your handling code here:
        limpiarDatos();
    }//GEN-LAST:event_bnLimpiarActionPerformed
    public void cambiarIdioma(Locale locale) {
        ResourceBundle mensajes = ResourceBundle.getBundle("mensajes.mensajes", locale);
        ResourceBundle titulos = ResourceBundle.getBundle("titulos.titulos", locale); 
            this.setTitle(titulos.getString("EliminarB"));
        codigoEliminar.setText(mensajes.getString("codigo"));
        direccionBibliotecaEliminar.setText(mensajes.getString("nombre"));
        direccionBibliotecaEliminar.setText(mensajes.getString("direccion"));
        telefonoBibliotecaEliminar.setText(mensajes.getString("telefono"));
        btBuscar.setText(mensajes.getString("botonBuscar"));
        btnEliminar.setText(mensajes.getString("eliminarBiblioteca"));
        bnLimpiar.setText(mensajes.getString("botonLimpiar"));
    }

