    private void btEditarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarClienteActionPerformed
        // TODO add your handling code here:
        String identificacion = txtIdentificacionCliente.getText();
        String nombre = txtNombreCliente.getText();
        String correo = txtCorreoCliente.getText();
        
        clienteControlador.update(nombre, identificacion, correo);
        JOptionPane.showMessageDialog(this, "Se actualizo correctamente!");
        limpiarDatos();
            
        
        
    }//GEN-LAST:event_btEditarClienteActionPerformed

    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        // TODO add your handling code here:
        String identificacion = txtIdentificacionCliente.getText();
        Cliente cliente = clienteControlador.read(identificacion);
        if (cliente==null){
            JOptionPane.showMessageDialog(this, "El cliente no existe!");
        }else{
            txtIdentificacionCliente.setEditable(false);
            txtIdentificacionCliente.setText(cliente.getIdentificacion());
            txtNombreCliente.setText(cliente.getNombre());
            txtNombreCliente.setEditable(true);
            txtCorreoCliente.setText(cliente.getCorreo());
            txtCorreoCliente.setEditable(true);

        }
    }
    private void limpiarDatos(){
        txtIdentificacionCliente.setEditable(true);
            txtIdentificacionCliente.setText("");
            txtNombreCliente.setText("");
            txtNombreCliente.setEditable(false);
            txtCorreoCliente.setText("");
            txtCorreoCliente.setEditable(false);
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

    private void bnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnLimpiarActionPerformed
        // TODO add your handling code here:
        limpiarDatos();
    }//GEN-LAST:event_bnLimpiarActionPerformed

