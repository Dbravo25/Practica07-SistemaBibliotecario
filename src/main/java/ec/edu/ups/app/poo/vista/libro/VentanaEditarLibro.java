    public void cambiarIdioma(Locale locale) {
        ResourceBundle mensajes = ResourceBundle.getBundle("mensajes.mensajes", locale);
        ResourceBundle titulos = ResourceBundle.getBundle("titulos.titulos", locale); 
            this.setTitle(titulos.getString("EditarL"));
        lbTituloLibro.setText(mensajes.getString("tituloLibro"));
        lbAutorLibro.setText(mensajes.getString("autorLibro"));
        lbAñoLibro.setText(mensajes.getString("anoLibro"));
        lbGenero.setText(mensajes.getString("Genero"));
        lbDisponibleLibro1.setText(mensajes.getString("disponibleLibro"));
        btnBuscar.setText(mensajes.getString("botonBuscar"));
        txtEditarLibro.setText(mensajes.getString("botonEditar"));
        jbLimpiar.setText(mensajes.getString("botonLimpiar"));
         lbCodigo.setText(mensajes.getString("codigo"));
    }

