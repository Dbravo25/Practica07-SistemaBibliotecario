    private void txtEditarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEditarLibroActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(txtCodigo.getText());
        Libro libro = libroControlador.read(codigo);
        String titulo = txtTituloEditar.getText();
        String autor = txtAutorEditar.getText();
        int año = Integer.parseInt(txtAñoEditar.getText());
        String genero = (String)jComboBoxGenero.getSelectedItem();
        String disponible = (String)disponibleBOX.getSelectedItem();
        libroControlador.update(codigo, titulo, autor, año, genero, disponible,libro.getNombiblioteca());
        JOptionPane.showMessageDialog(this, "Se actualizo correctamente!");
        limpiarDatos();
       
    }//GEN-LAST:event_txtEditarLibroActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
       int codigo = Integer.parseInt(txtCodigo.getText());
        Libro libro = libroControlador.read(codigo);
        if(libro!= null){
            txtTituloEditar.setText(libro.getTitulo());
            txtAutorEditar.setText(libro.getAutor());
            txtAñoEditar.setText(String.valueOf(libro.getAño()));
            disponibleBOX.setSelectedItem(String.valueOf(libro.getDisponible()));
            jComboBoxGenero.setSelectedItem(libro.getGenero());
            txtAutorEditar.setEditable(true);
            txtAñoEditar.setEditable(true);
            disponibleBOX.setEditable(true);
            jComboBoxGenero.setEditable(true);
            txtTituloEditar.setEditable(true);
            txtCodigo.setEditable(false);
            
        }else{
            JOptionPane.showMessageDialog(this, "El libro a editar no existe!");
        }
     
    }//GEN-LAST:event_btnBuscarActionPerformed

