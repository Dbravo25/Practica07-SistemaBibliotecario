    public void cargarDatos(){
        List<Cliente> clientes = clienteControlador.list();
        DefaultTableModel tabla =  (DefaultTableModel) tbListarCliente.getModel();
        tabla.setNumRows(0);
        for(Cliente cliente : clientes){
            Object[] rowData = {cliente.getIdentificacion(),cliente.getNombre(),cliente.getCorreo()};
            tabla.addRow(rowData);
            
        }
    }//GEN-LAST:event_btListarClienteActionPerformed

