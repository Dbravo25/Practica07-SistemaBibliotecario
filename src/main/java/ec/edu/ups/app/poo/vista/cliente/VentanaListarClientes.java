    public void cambiarIdioma(Locale locale) {
        ResourceBundle mensajes = ResourceBundle.getBundle("mensajes.mensajes", locale);
        ResourceBundle titulos = ResourceBundle.getBundle("titulos.titulos", locale);
        ResourceBundle tablas = ResourceBundle.getBundle("tablas.tablas", locale); 
        String indentificacion = tablas.getString( "tablaIdentificacion");
        String nombre = tablas.getString("tablaNombre");
        String correo = tablas.getString("tablaCorreo");
        DefaultTableModel tabla = (DefaultTableModel) tbListarCliente.getModel();
        tabla.setColumnIdentifiers(new String [] {indentificacion,nombre,correo});
            this.setTitle(titulos.getString("ListarC"));
        btListarCliente.setText(mensajes.getString("listarBiblioteca"));
    }

