    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        // TODO add your handling code here:
        String identificacion = txtIdentificacionEliminar.getText();
        Cliente cliente = clienteControlador.read(identificacion);
        if (cliente==null){
            JOptionPane.showMessageDialog(this, "El cliente no existe!");
        }else{
            txtIdentificacionEliminar.setEditable(false);
            txtIdentificacionEliminar.setText(cliente.getIdentificacion());
            txtNombreCliente.setText(cliente.getNombre());
            txtNombreCliente.setEditable(true);
            txtCorreoCliente.setText(cliente.getCorreo());
            txtCorreoCliente.setEditable(true);

        }
        }
        private void limpiarDatos(){
            txtIdentificacionEliminar.setEditable(true);
            txtIdentificacionEliminar.setText("");
            txtNombreCliente.setText("");
            txtNombreCliente.setEditable(false);
            txtCorreoCliente.setText("");
            txtCorreoCliente.setEditable(false);
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

        public void cambiarIdioma(Locale locale) {
        ResourceBundle mensajes = ResourceBundle.getBundle("mensajes.mensajes", locale);
        ResourceBundle titulos = ResourceBundle.getBundle("titulos.titulos", locale); 
            this.setTitle(titulos.getString("EliminarC"));
        codigoEliminar.setText(mensajes.getString("idCliente"));
        lbNombreCliente.setText(mensajes.getString("nombre"));
        lbCorreoCliente.setText(mensajes.getString("correoCliente"));
        btnBuscarCliente.setText(mensajes.getString("buscarBibliotecaMenu"));      
        btnEliminarIdentificacion.setText(mensajes.getString("eliminarBiblioteca"));
        bnLimpiar.setText(mensajes.getString("botonLimpiar"));
    }

