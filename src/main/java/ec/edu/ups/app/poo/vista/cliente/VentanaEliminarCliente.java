    private void btnEliminarIdentificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarIdentificacionActionPerformed
        // TODO add your handling code here:
        String identificacion = txtIdentificacionEliminar.getText();
        int respuesta = JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar el Cliente");
        if(respuesta== JOptionPane.YES_OPTION){
        clienteControlador.delete(identificacion);
        JOptionPane.showMessageDialog(this, "Se ha eliminado correctamente!");
        limpiarDatos();
        }
         
    }//GEN-LAST:event_btnEliminarIdentificacionActionPerformed

